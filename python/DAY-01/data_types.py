#data types: data types are used to specify the type of a variable
#data types are used to check the type of a variable
#1. string  e.g. "kashif"
#2. integer e.g. 20
#3. float e.g. 20.5
#4. boolean e.g. True or False
#5. complex e.g. 2+3j



name = "kashif" # string
age = 20 # integer
is_married = False # boolean
address = "kamal khel kohat" # string

print(type(name))  # <class 'str'>
print(type(age))  # <class 'int'>
print(type(is_married))  # <class 'bool'>
print(type(address))  # <class 'str'>